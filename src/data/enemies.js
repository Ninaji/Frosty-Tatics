const atk = (pt, dice, dtype, opts = {}) => ({ pt, dice, dtype, range: 1, ...opts });
const xd = (element, dice, chance) => ({ type: 'extraDamage', element, dice, ...(chance != null ? { chance } : {}) });
const rider = (condition, chance, dc = 12, save = 'con', duration = 2) =>
  ({ type: 'riderCondition', condition, chance, dc, save, duration });

// specials: habilidades ativas usadas pela IA
const smite = (pt, dice, dtype, opts = {}) => ({ pt, kind: 'smite', dice, dtype, range: 1, cooldown: 3, ...opts });
const blast = (pt, dice, dtype, opts = {}) => ({ pt, kind: 'blast', dice, dtype, range: 4, radius: 1, save: 'dex', dc: 12, cooldown: 3, ...opts });
const healAlly = (pt, dice, opts = {}) => ({ pt, kind: 'heal', dice, range: 4, cooldown: 3, ...opts });
const buff = (pt, condition, opts = {}) => ({ pt, kind: 'buff', condition, duration: 3, range: 4, cooldown: 4, ...opts });
const debuff = (pt, condition, opts = {}) => ({ pt, kind: 'debuff', condition, duration: 2, range: 4, save: 'wis', dc: 12, cooldown: 3, ...opts });
const summon = (pt, summonId, count, opts = {}) => ({ pt, kind: 'summon', summonId, count, uses: 1, cooldown: 5, ...opts });

function E(id, pt, gender, family, tier, def) {
  return {
    id, pt, gender, family, tier,
    str: 10, dex: 10, con: 10, int: 8, wis: 8, cha: 8,
    speed: 5, behavior: 'agressivo',
    traits: [], specials: [], resist: [], vuln: [], immune: [], condImmune: [],
    ...def,
  };
}

